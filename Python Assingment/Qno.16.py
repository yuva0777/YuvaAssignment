menu = input("Enter your choice (Pizza, Burger, Pasta): ")
if menu == "Pizza":
    print("The price of Pizza is $10")
elif menu == "Burger":
    print("The price of Burger is $7")
elif menu == "Pasta":
    print("The price of Pasta is $8")
else:
    print("Invalid choice. Please select Pizza, Burger, or Pasta.")